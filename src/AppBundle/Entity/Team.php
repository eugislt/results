<?php

namespace AppBundle\Entity;

class Team
{

    public function __construct($id, $name)
    {
        $this->id = $id;
        $this->name = $name;
    }
    /**
     * @var int
     */
    private $id;

    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     * @return Team
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}